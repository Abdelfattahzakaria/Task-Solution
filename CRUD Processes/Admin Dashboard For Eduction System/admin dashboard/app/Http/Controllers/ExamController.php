<?php

namespace App\Http\Controllers;

use App\Http\Requests\ExamRequest;
use App\Repositery\ExamsRepositeryInterface;
use Illuminate\Http\Request;

class ExamController extends Controller
{
    protected $Exam;
    public function __construct(ExamsRepositeryInterface $Exam)
    {
        $this->Exam = $Exam;
    }
    //-------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------
    public function examsIndex()
    {
        return $this->Exam->exam_index();
    }
    //-------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------
    public function examsCreate()
    {
        return $this->Exam->exam_create();
    }
    //-------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------
    public function examsStore(ExamRequest $request)
    {
        return $this->Exam->exam_store($request);
    }
    //-------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------
    public function examEdit($id)
    {
        return $this->Exam->exam_edit($id);
    }
    //-------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------
    public function examUpdate(ExamRequest $request)
    {
        return $this->Exam->exam_update($request);
    }
    //-------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------
    public function examDelete(Request $request)
    {
        return $this->Exam->exam_delete($request);
    }
}
 