<?php

namespace App\Http\Controllers;

use App\Http\Requests\QuizRequest;
use App\Models\Quiz;
use App\Repositery\QuizRepositeryInterface;
use Illuminate\Http\Request;

class QuizController extends Controller
{
    protected $Quiz;
    public function __construct(QuizRepositeryInterface $Quiz)
    {
        $this->Quiz = $Quiz;
    }
    //-------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------
    public function quizIndex()
    {
        return $this->Quiz->index();
    }
    //-------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------
    public function quizCreate()
    {
        return $this->Quiz->create();
    }
    //-------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------
    public function quizStore(QuizRequest $request)
    {
        return $this->Quiz->store($request);
    }
    //-------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------
    public function quizEdit($id)
    {
        return $this->Quiz->edit($id);
    }
    //-------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------
    public function quizUpdate(QuizRequest $request)
    {
        return $this->Quiz->update($request);
    }
    //-------------------------------------------------------------------------------------------------------------------
    //-------------------------------------------------------------------------------------------------------------------
    public function quizDelete(Request $request)
    {
        return $this->Quiz->delete($request);
    }
}
