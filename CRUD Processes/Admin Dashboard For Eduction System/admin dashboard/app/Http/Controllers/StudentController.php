<?php

namespace App\Http\Controllers;

use App\Http\Requests\StudentRequest;
use App\Repositery\StudentRepositeryInterface;
use Illuminate\Http\Request;

class StudentController extends Controller
{
  protected $Student;
  public function __construct(StudentRepositeryInterface $Student)
  {
    $this->Student = $Student;
  }
  //-------------------------------------------------------------------------------------------------------------------
  //-------------------------------------------------------------------------------------------------------------------  
  public function createStudent()
  {
    return $this->Student->create_student();
  }
  //-------------------------------------------------------------------------------------------------------------------
  //-------------------------------------------------------------------------------------------------------------------  
  public function Get_classrooms($id)
  {
    return $this->Student->get_classrooms($id); 
  }
  //-------------------------------------------------------------------------------------------------------------------
  //-------------------------------------------------------------------------------------------------------------------  
  public function Get_Sections($id)
  {
    return $this->Student->get_Sections($id);
  }
  //-------------------------------------------------------------------------------------------------------------------
  //-------------------------------------------------------------------------------------------------------------------  
  public function studentStore(StudentRequest $request)
  {
    //return $request;
    return $this->Student->store($request);
  }
  //-------------------------------------------------------------------------------------------------------------------
  //------------------------------------------------------------------------------------------------------------------- 
  public function studentList()
  {
    return $this->Student->get_Students();
  }
  //-------------------------------------------------------------------------------------------------------------------
  //-------------------------------------------------------------------------------------------------------------------
  public function studentEdit($id)
  {
    return $this->Student->student_edit($id);
  }
  //-------------------------------------------------------------------------------------------------------------------
  //-------------------------------------------------------------------------------------------------------------------
  public function studentUpdate(StudentRequest $request)
  {
    return $this->Student->student_update($request);
  }
  //-------------------------------------------------------------------------------------------------------------------
  //-------------------------------------------------------------------------------------------------------------------
  public function studentDelete(Request $request)
  {
    return $this->Student->student_delete($request);
  }
  //-------------------------------------------------------------------------------------------------------------------
  //-------------------------------------------------------------------------------------------------------------------
  public function showStudentDetails($id)
  {
    return $this->Student->show_student_details($id);
  }
  //-------------------------------------------------------------------------------------------------------------------
  //-------------------------------------------------------------------------------------------------------------------
  public function uploadAttachments(Request $request)
  {
    return $this->Student->upload_attachments($request);
  }
  //-------------------------------------------------------------------------------------------------------------------
  //-------------------------------------------------------------------------------------------------------------------
  public function downloadAttachment($studentName, $fileName)
  {
    return $this->Student->download_student_attachment($studentName, $fileName);
  }
  //-------------------------------------------------------------------------------------------------------------------
  //-------------------------------------------------------------------------------------------------------------------
  public function deleteAttchment(Request $request)
  {
     return $this->Student->delete_student_attachment($request);  
  }
}
 